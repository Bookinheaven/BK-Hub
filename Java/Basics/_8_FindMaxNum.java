public class _8_FindMaxNum {
    public static void main(String[] args) {
        int num_1 = 10;
        int num_2 = 10;

        if (num_1 > num_2){
            System.out.println("Num1 "+ num_1+" > Num2 "+num_2);
        }
        else if (num_1 < num_2){
            System.out.println("Num1 "+ num_1+" < Num2 "+num_2);
        }
        else {
            System.out.println("Num1 "+ num_1+" = Num2 "+num_2);
            
        }
    }
}
